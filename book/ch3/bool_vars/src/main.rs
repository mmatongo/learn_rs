fn main() {
    let truth = true;
    let lie = false;
    println!("truth is {}", truth);
    println!("lie is {}", lie);

    // but we can also use arithmetic to determine this
    let truth = 5 > 2;
    let lie = -3 >= 8;

    println!("truth: {} - false: {}", truth, lie);

    /*
     * ==: is equal to
     * !=: is different from
     * <: is less than
     * <=: is less than or equal to
     * >: is greater than
     * >=: is greater than or equal to
     */

}
